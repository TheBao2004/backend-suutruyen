function removePathPublic ($folders) {
    $folders = array_map(function($item) {
        if($item !== "public/.gitignore"){
            return substr($item, 7);
        }
    }, $folders);
    return array_unique(array_filter($folders));
}


function getItemFinal ($path, $char = '/') {
    $arr = explode($char, $path);
    return $arr[count($arr) - 1];
}

function getBreadcrumb ($str) {
    if(empty($str)) return null;
    $arr = explode('/', $str);
    $count = count($arr) - 1;
    $response = null;
    foreach ($arr as $k => $v) {
        $response[$k] = '';
        for ($i=0; $i <= $count; $i++) {
            $response[$k] .= $arr[$i].'/';
        }
        $response[$k] = trim($response[$k], '/');
        $count--;
    }
    $response = array_reverse($response);
    $response = [
        'path' => $response,
        'show' => $arr
    ];
    return $response;
}
